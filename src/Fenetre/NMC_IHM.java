    private void bt_excelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_excelActionPerformed
        
        try {
           Metier.WriteExcel writeExcel = new WriteExcel(); 
            FileDialog fd = new FileDialog(NMC_IHM.this, "Save file", FileDialog.SAVE);
            fd.show();
            if(fd.getFile() != null){
            nfile = fd.getDirectory() + fd.getFile() + ".xls"; 
                try {
                   writeExcel.setOutputFile(nfile);
//                   System.out.println("Please check the result file under " + nfile);
                writeExcel.write(Integer.parseInt(txt_mttl.getText()), Integer.parseInt(txt_mtth.getText()), Integer.parseInt(txt_mttc.getText()), dt_Start.getDate().toString()+ " to " + dt_End.getDate().toString());
                Runtime.getRuntime().exec("rundll32 url.dll,FileProtocolHeader"+nfile);
                    } catch (IOException ex) {
                        JOptionPane.showMessageDialog(null, "Nom du fichier existant et ouvert dans un autre processus");
                    } catch (WriteException ex) {
                        JOptionPane.showMessageDialog(null, "Probleme d'ecriture : " + ex.getMessage());
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Nom du fichier existant et ouvert dans un autre processus"); 
                    }
            } 
            } catch (Exception e) {
                   JOptionPane.showMessageDialog(null, "Probleme du write : " + e.getMessage()); 
                }  
    }//GEN-LAST:event_bt_excelActionPerformed

    private void bt_graphActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_graphActionPerformed
        try {
            
            if(rb_NC.isSelected()){
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.setValue(Double.parseDouble(txt_mttl.getText()), "Temps", "MTTL");
        dataset.setValue(Double.parseDouble(txt_mtth.getText()), "Temps", "MTTH");
        dataset.setValue(Double.parseDouble(txt_mttc.getText()), "Temps", "MTTC");
        JFreeChart chart = ChartFactory.createBarChart("NMC Workforce", "Category", "Times / Min", dataset, PlotOrientation.VERTICAL, false, true, false);
        CategoryPlot categoryPlot = chart.getCategoryPlot();
        categoryPlot.setRangeGridlinePaint(Color.black);
        ChartFrame frame = new ChartFrame("NMC Workforce Graphics for NC", chart);
        frame.setVisible(true);
//        frame.set
        frame.setSize(450, 350);
        
        }
        else if (rb_OPS.isSelected()){
        
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.setValue(Double.parseDouble(txt_mtta.getText()), "Temps", "MTTA");
        dataset.setValue(Double.parseDouble(txt_mttr.getText()), "Temps", "MTTR");
        JFreeChart chart = ChartFactory.createBarChart("NMC Workforce", "Category", "Times / Min", dataset, PlotOrientation.VERTICAL, false, true, false);
        CategoryPlot categoryPlot = chart.getCategoryPlot();
        categoryPlot.setRangeGridlinePaint(Color.black);
        ChartFrame frame = new ChartFrame("NMC Workforce Graphics for OPS", chart);
        frame.setVisible(true);
//        frame.set
        frame.setSize(450, 350);
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please complete all fields for the filter or restart");
        }
        
    }//GEN-LAST:event_bt_graphActionPerformed

