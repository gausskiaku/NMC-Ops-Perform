    private void bt_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_searchActionPerformed
        if(dt_Start.getDate() == null || dt_End.getDate() == null || cb_allUser.getSelectedIndex() == -1 || cb_allUser.getSelectedIndex() == 0 || dt_Start.getDate().after(dt_End.getDate())){
            
            JOptionPane.showMessageDialog(null, "Please complete all fields for the filter or verify date");
        } else {
            DateTime dateJodaStart = new DateTime(dt_Start.getDate());
            DateTime dateJodaEnd = new DateTime(dt_End.getDate());
            String dateStart = dateJodaStart.toString("M/d/yyyy");
            String dateEnd = dateJodaEnd.toString("M/d/yyyy");
//            JOptionPane.showMessageDialog(null, dateEnd);
        //   NMC Network
        if(rb_NC.isSelected()){
            try {
                txt_mttl.setText(String.valueOf(MTTL.moyenneMTTL(cb_allUser.getSelectedItem().toString(), dateStart, dateEnd,txt_nbreTotalTicket, ctx)));
                bt_graph.setEnabled(true);
                bt_excel.setEnabled(true);
            } catch (ArithmeticException e) {
//                JOptionPane.showMessageDialog(null, "Aucun Ticket retouver");
                txt_mttl.setText("0");
                bt_graph.setEnabled(false);
                bt_excel.setEnabled(false);
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "PROBLEME : " + e.getMessage());
            
            }
            
            try {
                txt_mtth.setText(String.valueOf(MTTH.moyenneMTTH(cb_allUser.getSelectedItem().toString(), dateStart, dateEnd, ctx)));
            } catch (ArithmeticException e) {
//                JOptionPane.showMessageDialog(null, "Aucun Ticket retouver");
                txt_mtth.setText("0");
            } catch (Exception e){
            JOptionPane.showMessageDialog(null, "PROBLEME : " + e.getMessage());
            }
            
            try {
                txt_mttc.setText(String.valueOf(MTTC.moyenneMTTC(cb_allUser.getSelectedItem().toString(), dateStart, dateEnd,txt_nbreClosedTicket, ctx)));
            } catch (ArithmeticException e) {
//                JOptionPane.showMessageDialog(null, "Aucun Ticket retouver");
                txt_mttc.setText("0");
            } catch (Exception e){
                 JOptionPane.showMessageDialog(null, "PROBLEME CLOSED : " + e);
            }
        } 
        
        // OPS
        else if (rb_OPS.isSelected()){
        
        try {
                txt_mttr.setText(String.valueOf(MTTR.moyenneMTTR(txt_fullName.getText(), dateStart, dateEnd, ctx)));
            } catch (ArithmeticException e) {
//                JOptionPane.showMessageDialog(null, "Aucun Ticket retouver");
                txt_mttr.setText("0");
            } catch (Exception e){
            JOptionPane.showMessageDialog(null, "PROBLEME : " + e.getMessage());
            }
            
            try {
                txt_mtta.setText(String.valueOf(MTTA.moyenneMTTA(txt_fullName.getText(), dateStart, dateEnd, ctx)));
            } catch (ArithmeticException e) {
//                JOptionPane.showMessageDialog(null, "Aucun Ticket retouver");
                txt_mtta.setText("0");
            } catch (Exception e){
                 JOptionPane.showMessageDialog(null, "PROBLEME : " + e.getMessage());
            }
        
        }
          
        }
        
        
    }//GEN-LAST:event_bt_searchActionPerformed
